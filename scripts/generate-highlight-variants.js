#!/usr/bin/env node
import fs from 'fs';
import path from 'path';

const variantsDir = 'src/components/highlight/variants';
const outputFile = 'src/components/highlight/highlight-variants.ts';

const variants = {};

// Read all SVG files
for (let i = 1; i <= 20; i++) {
  const svgPath = path.join(variantsDir, `${i}.svg`);
  const svgContent = fs.readFileSync(svgPath, 'utf-8');

  // Convert to base64
  const base64 = Buffer.from(svgContent).toString('base64');
  variants[i] = `data:image/svg+xml;base64,${base64}`;
}

// Generate TypeScript file
const tsContent = `// Auto-generated by scripts/generate-highlight-variants.js
// Do not edit manually

export const highlightVariants: Record<number, string> = ${JSON.stringify(variants, null, 2)};
`;

fs.writeFileSync(outputFile, tsContent);
console.log(
  `âœ… Generated ${outputFile} with ${Object.keys(variants).length} variants`
);
